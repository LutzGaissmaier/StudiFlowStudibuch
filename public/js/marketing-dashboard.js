// NAVIGATION SYSTEM
function showSection(sectionName) {
  console.log(`üîÑ Switching to section: ${sectionName}`);
  
  // Hide all sections
  document.querySelectorAll('.content-section').forEach(section => {
    section.style.display = 'none';
  });
  
  // Remove active class from all tabs
  document.querySelectorAll('.nav-tab').forEach(tab => {
    tab.classList.remove('active');
  });
  
  // Show selected section
  const section = document.getElementById(sectionName + '-section');
  if (section) {
    section.style.display = 'block';
    console.log(`‚úÖ Section ${sectionName} angezeigt`);
  } else {
    console.error(`‚ùå Section ${sectionName} nicht gefunden!`);
  }
  
  // Add active class to selected tab
  const tab = document.querySelector(`[data-section="${sectionName}"]`);
  if (tab) {
    tab.classList.add('active');
  }
  
  // Update debug info
  const debugSection = document.getElementById('debug-section');
  if (debugSection) {
    debugSection.textContent = sectionName;
  }
  
  DashboardState.currentSection = sectionName;
  
  // Re-attach button handlers for the new section
  setTimeout(() => {
    attachAllButtonHandlers();
  }, 100);
}

// BUTTON HANDLER ATTACHMENT - SUPER ROBUST
function attachAllButtonHandlers() {
  console.log('üîß attachAllButtonHandlers gestartet...');
  
  // Review Action Buttons
  const reviewButtons = document.querySelectorAll('.review-actions button');
  console.log(`üìã Gefunden: ${reviewButtons.length} Review-Buttons`);
  
  reviewButtons.forEach((button, index) => {
    const text = button.textContent.trim();
    const onclickAttr = button.getAttribute('onclick');
    console.log(`üîò Button ${index + 1}: "${text}" | onclick: ${onclickAttr}`);
    
    // Remove existing event listeners
    button.replaceWith(button.cloneNode(true));
    const newButton = document.querySelectorAll('.review-actions button')[index];
    
    // Add click handler based on button text
    if (text.includes('Genehmigen')) {
      const itemId = extractItemId(onclickAttr) || `item${index + 1}`;
      newButton.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();
        console.log(`‚úÖ CLICK: Genehmigen f√ºr ${itemId}`);
        approveContent(itemId);
      });
    } else if (text.includes('Bearbeiten')) {
      const itemId = extractItemId(onclickAttr) || `item${index + 1}`;
      newButton.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();
        console.log(`‚úèÔ∏è CLICK: Bearbeiten f√ºr ${itemId}`);
        editContent(itemId);
      });
    } else if (text.includes('Ablehnen')) {
      const itemId = extractItemId(onclickAttr) || `item${index + 1}`;
      newButton.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();
        console.log(`‚ùå CLICK: Ablehnen f√ºr ${itemId}`);
        rejectContent(itemId);
      });
    } else if (text.includes('Planen')) {
      const itemId = extractItemId(onclickAttr) || `item${index + 1}`;
      newButton.addEventListener('click', (e) => {
        e.preventDefault();
        e.stopPropagation();
        console.log(`üìÖ CLICK: Planen f√ºr ${itemId}`);
        scheduleContent(itemId);
      });
    }
    
    // Visual feedback
    newButton.addEventListener('click', () => {
      newButton.style.transform = 'scale(0.95)';
      setTimeout(() => {
        newButton.style.transform = 'scale(1)';
      }, 150);
    });
    
    // Ensure button is clickable
    newButton.style.pointerEvents = 'auto';
    newButton.style.cursor = 'pointer';
    newButton.style.position = 'relative';
    newButton.style.zIndex = '100';
    
    console.log(`‚úÖ Handler f√ºr "${text}" erfolgreich attachiert`);
  });
  
  // Header Buttons
  attachHeaderButtons();
  
  // Navigation Tabs
  attachNavigationTabs();
  
  // Engagement Buttons
  attachEngagementButtons();
  
  // FAB Menu
  attachFABMenu();
  
  console.log('‚úÖ Alle Button-Handler erfolgreich attachiert!');
}

function extractItemId(onclickAttr) {
  if (!onclickAttr) return null;
  const match = onclickAttr.match(/'([^']+)'/);
  return match ? match[1] : null;
}

