// Toast Notification System
function showToast(message, type = 'info', duration = 4000) {
  console.log(`üì¢ Toast: ${message} (${type})`);
  
  let container = document.getElementById('toast-container');
  if (!container) {
    container = document.createElement('div');
    container.id = 'toast-container';
    container.style.cssText = 'position: fixed; top: 20px; right: 20px; z-index: 10000; display: flex; flex-direction: column; gap: 10px;';
    document.body.appendChild(container);
  }
  
  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.style.cssText = `
    background: white; 
    border-radius: 8px; 
    box-shadow: 0 10px 25px rgba(0,0,0,0.15); 
    padding: 16px; 
    border-left: 4px solid ${getToastColor(type)}; 
    min-width: 300px; 
    transform: translateX(100%); 
    transition: transform 0.3s ease;
    display: flex;
    align-items: center;
    gap: 12px;
    font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', sans-serif;
    font-size: 14px;
    color: #374151;
  `;
  
  toast.innerHTML = `
    <span style="font-size: 20px;">${getToastIcon(type)}</span>
    <span style="flex: 1;">${message}</span>
    <button onclick="this.parentElement.remove()" style="background: none; border: none; font-size: 18px; cursor: pointer; color: #6b7280;">√ó</button>
  `;
  
  container.appendChild(toast);
  
  // Animate in
  setTimeout(() => {
    toast.style.transform = 'translateX(0)';
  }, 100);
  
  // Auto remove
  setTimeout(() => {
    toast.style.transform = 'translateX(100%)';
    setTimeout(() => toast.remove(), 300);
  }, duration);
}

function getToastIcon(type) {
  const icons = {
    'success': '‚úÖ',
    'error': '‚ùå', 
    'warning': '‚ö†Ô∏è',
    'info': '‚ÑπÔ∏è'
  };
  return icons[type] || '‚ÑπÔ∏è';
}

function getToastColor(type) {
  const colors = {
    'success': '#10b981',
    'error': '#ef4444',
    'warning': '#f59e0b',
    'info': '#3b82f6'
  };
  return colors[type] || '#3b82f6';
}

